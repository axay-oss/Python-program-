# Addition two numbers
a = 10
b = 20
sum = a + b
print("Sum is:", sum)

# Variables and their Types
a = 10
b = 10.5
c = "Hello"
d = True

print(type(a))
print(type(b))
print(type(c))
print(type(d))

# Type Casting
a = 10
b = float(a)   # int to float
c = str(a)     # int to string
print(b, type(b))
print(c, type(c))

# Logical Operators
a = True
b = False

print("AND:", a and b)
print("OR:", a or b)
print("NOT:", not a)
